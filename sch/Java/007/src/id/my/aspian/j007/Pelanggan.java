    private void refresh() {
        Utils.clearTable(tableModel);
        try (ResultSet rs = conn.createStatement().executeQuery("SELECT * FROM pelanggan")) {
            while (rs.next()) {
                tableModel.addRow(new String[] {
                        rs.getString("kode"), rs.getString("nama")
                });
            }
            
            Utils.clearInputs(kode, nama);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

