    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int row = table.getSelectedRow();
        kode.setText(tableModel.getValueAt(row, 0).toString());
        nama.setText(tableModel.getValueAt(row, 1).toString());
    }//GEN-LAST:event_tableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        try (PreparedStatement stmt = conn.prepareStatement("INSERT INTO pelanggan (kode, nama) VALUES (?, ?)")) {
            stmt.setString(1, kode.getText());
            stmt.setString(2, nama.getText());
            stmt.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            refresh();
        }
    }// GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        try (PreparedStatement stmt = conn.prepareStatement("UPDATE pelanggan SET nama = ? WHERE kode = ?")) {
            stmt.setString(1, nama.getText());
            stmt.setString(2, kode.getText());
            stmt.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            refresh();
        }
    }// GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton3ActionPerformed
        try (PreparedStatement stmt = conn.prepareStatement("DELETE FROM pelanggan WHERE kode = ?")) {
            stmt.setString(1, kode.getText());
            stmt.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            refresh();
        }
    }// GEN-LAST:event_jButton3ActionPerformed

